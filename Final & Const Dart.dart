void main() {
  // Final & Const is a variable that immutable

//Declare Const Variable:
  const phi = 3.14;
  const double bar = 1000.55;
  const int value = 22;
  const Firstname = "Izza";

  print(phi);
  print(bar);
  print(value);
  print("$Firstname\n");

  //Declare Final Variable
  final name = "Auliya Sayang";
  final double qi = 15.7;
  final int c = 26;
  final String lastname = "Chryslin May";
  // name = "Ramadhani";

  print(name);
  print("$qi");
  print("$c");
  print("object");
  print("Hello my name is" + "$lastname");
}
