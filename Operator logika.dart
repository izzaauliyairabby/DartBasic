void main() {
  /*
  Operator logika adalah operator yang digunakan
  untuk membandingkan 2 kondisi logika. yakni benar (True)
  atau salah (False)

  Logical operators are operators used
  to compare 2 logical conditions. i.e. true (True)
  or false (False)
  */

    int a = 5;
    int b = 7;
 
    // Using And Operator
    bool c = a > 10 && b < 10;
    print(c);
 
    // Using Or Operator
    bool d = a > 10 || b < 10;
    print(d);
 
    // Using Not Operator
    bool e = !(a > 10);
    print(e);

}
