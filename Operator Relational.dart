// Membandingkan nilai-nilai yang dijadikan operand
void main() {
  int a = 10;
  int b = 10;
  int c = 12;
  int d = 5;
  double e = 25.5;
  double f = 25.5;

// =
  print("Is a is = b ? ${a == b}");

// !=
  print("Is a is != c ? ${a != c}");

// <
  print("Is a is < d ? ${a > d}");

// >
  print("Is b is > d ? ${b > d}");

// <=
  print("Is e is <= f ? ${e <= f}");

// >=
  print("Is f is >= e ? ${f >= e}");
}
